.MODEL SMALL
.DATA
MSG DB "HELLO WORLD$"
MSG1 DB "CHARACTER PRESENT$"
MSG2 DB "CHARACTER NOT PRESENT$"
MSG3 DB 10,13,'$'
INS DB "Insert Letter: $"


.CODE
MOV AX, @DATA
MOV DS, AX

LEA SI,MSG

LEA DX, INS
MOV AH,09h
INT 21h

MOV AH,01h
INT 21h

LEA DX, MSG3
MOV AH,09h
INT 21h

MOV BL,'$'

UP: CMP BL,[SI]
JZ EXIT
CMP AL,[SI]
JZ PRINT
INC SI
JMP UP

PRINT: LEA DX, MSG1
MOV AH,09h
INT 21h
MOV AH,4Ch
INT 21h

EXIT: LEA DX, MSG2
MOV AH,09h
INT 21h
MOV AH,4Ch
INT 21h

END
